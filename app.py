import streamlit as st
from pypdf import PdfReader
import google.generativeai as genai
import os
import re
from fpdf import FPDF
import docx2txt
import plotly.graph_objects as go

# 1. Branding Settings
st.set_page_config(
    page_title="getshortlisted.online | AI Resume Analyzer", 
    page_icon="📄", 
    layout="centered"
)

# API Setup
my_key = os.environ.get("MY_API_KEY")
genai.configure(api_key=my_key)
model = genai.GenerativeModel('gemini-2.5-flash') # Updated to latest stable

# 1. New Function for DOCX
def extract_text_from_docx(file):
    return docx2txt.process(file)

# 2. PDF Creation Function
def create_pdf(text):
    pdf = FPDF()
    pdf.add_page()
    pdf.set_font("Arial", 'B', 16)
    pdf.set_text_color(0, 51, 102)
    pdf.cell(0, 10, "ATS ANALYSIS REPORT", ln=True, align='C')
    pdf.line(10, 22, 200, 22)
    pdf.ln(10)
    
    pdf.set_text_color(0, 0, 0)
    pdf.set_font("Arial", size=11)
    clean_text = text.replace("**", "").replace("###", "").replace("##", "").replace("#", "").replace("*", "-")
    # Multi_cell handles long text
    pdf.multi_cell(0, 8, txt=clean_text.encode('latin-1', 'ignore').decode('latin-1'))
    
    pdf.set_y(-20)
    pdf.set_font("Arial", 'I', 8)
    pdf.cell(0, 10, "Generated by getshortlisted.online", align='C')
    return pdf.output(dest='S').encode('latin-1')

